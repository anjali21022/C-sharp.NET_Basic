using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Methods_and_Properties_of_Console_Class_in_C_sharp
{
    internal class Write_and_WriteLine_Method
    {
        static void Main(string[] args)
        {
            //Write Method Print the value and stay in the same line
            Console.Write("Hello ");
            //WriteLine Method Print the value and move the cursor to the next line
            Console.WriteLine("Anjali");
            Console.Write("How are you?");
            Console.ReadKey();
        }
    }
}
